    stat, p = shapiro(data)
    print(f"\nüîé Shapiro-Wilk Test for '{col}':")
    print(f"Test Statistic = {stat:.4f}, p-value = {p:.4f}")
    if p > 0.05:
        print("Likely normal (fail to reject H‚ÇÄ)")
    else:
        print("Not normal (reject H‚ÇÄ)")

